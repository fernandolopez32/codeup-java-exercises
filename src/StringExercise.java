public class StringExercise {
    public static void main(String[] args) {
        String one ="We don't need no education \nWe don't need no thought control";
        System.out.println(one);

        String string = "check \"this\" out!, \"s inside of \"s!";
        System.out.println(string);

        String window = "In windows, the main drive is usually C:\\";
        System.out.println(window);

        String slash = "I can do backslashes \\, double backslashes \\\\,\nand the amazing triple backslash \\\\\\!";
        System.out.println(slash);
    }
}
